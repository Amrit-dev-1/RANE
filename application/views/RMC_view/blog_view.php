    <h4 class="blog-title">Village Forms VI, VII-XII and Other Revenue Maps</h4>

    <h2 class="blog-h2">Village Form VI (Register of Mutation):</h2>
    <p class="blog-point">Records changes in the Record of Right due to transfers such as sale, mortgage, lease, exchange, gift, or inheritance.</p>

    <h2 class="blog-h2">Village Forms VII-XII (Combined Record of Right and Crops):</h2>
    <p class="blog-point">Records details of land occupants, tenure, local names of fields, assessment, rent, and other rights.</p>

    <h2 class="blog-h2">Village Form VIII-A:</h2>
    <p class="blog-point">Details of khatedars' land with the area and taxes payable.</p>

    <h2 class="blog-h2">Village Forms VI-C (Register of Heirship Cases):</h2>
    <p class="blog-point">Records details of deceased occupants, legal heirs, and decisions by the Tahsildar on heirship matters.</p>

    <h2 class="blog-h2">Village Form VII-B:</h2>
    <p class="blog-point">Records names of individuals in possession of land, differing from the Record of Rights. Initiates the process of investigation by the Tahsildar.</p>

