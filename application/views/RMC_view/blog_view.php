    <h3 class="blog-h3">Village Maps:</h3>
    <p class="blog-point">Detailed maps illustrating the geographical layout of a village, including the location of individual land parcels, roads, water bodies, and other features.</p>

    <h3 class="blog-h3">Gut Book Maps:</h3>
    <p class="blog-point">Maps associated with "Gut Books," which typically contain details about landownership, cultivation, and revenue assessments. The maps in Gut Books help visualize the distribution of land within a particular area.</p>


